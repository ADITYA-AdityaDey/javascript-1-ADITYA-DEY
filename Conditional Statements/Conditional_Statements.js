const score = 0

if (score > 0) {
    console.log("Score is Positive")
}else if (score < 0) {
    console.log("Score is Not Positive")
}else {
    console.log("Score is ZERO")
}



const color = "red"

switch(color) {
    case "red":
        console.log("color is red")
        break
    case "blue":
        console.log("color is blue")
        break
    case "green":
        console.log("color is green")
        break
    default:
        console.log("not a valid color")
}