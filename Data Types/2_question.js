// Use typeof operator and apply on multiple variables.

// Strings
const userName = "First User";
console.log(userName);
console.log(typeof(userName));


// Numbers
const userCode = 1;
console.log(userCode);
console.log(typeof(userCode));


// Booleans
const isUserLoggedIn = true;
console.log(isUserLoggedIn);
console.log(typeof(isUserLoggedIn));


// Object
const userDetails = {firstName: "First", lastName: "User"};
console.log(userDetails);
console.log(typeof(userDetails));



// Array Object
const papers = ["DSA", "DAA", "c", "OS", "COMPUTER NETWORKS", "OOPS", "DBMS"];
console.log(papers);
console.log(typeof(papers));